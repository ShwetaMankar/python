import numpy
a = numpy.array([[1,2],[3,4]])
b = numpy.array([[5,6],[7,8]])

print("Element wise addition of matrix is : ")
print(numpy.add(a,b))

print("Element wise subtraction of matrix is : ")
print(numpy.subtract(a,b))

print("Element wise division of matrix is : ")
print(numpy.divide(a,b))

print("Element wise multiplication of matrix is : ")
print(numpy.multiply(a,b))